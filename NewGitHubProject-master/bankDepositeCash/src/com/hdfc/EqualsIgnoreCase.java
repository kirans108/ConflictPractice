package com.hdfc;

public class EqualsIgnoreCase {
	public static void main (String args[]) {
		String s1="Javaclass";
		String s2="JavaClass";
		String s3="Javaclass";
		String s4="Python";
		System.out.println(s1.equalsIgnoreCase(s2));
		System.out.println(s1.equalsIgnoreCase(s3));
		System.out.println(s3.equalsIgnoreCase(s4));
	

}}
